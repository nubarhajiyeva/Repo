public class RiyaziMentiqiHesablamalar {
    public static void main (String args[]) {
        int a=78;
        int b=43;
        System.out.println("a+b="+(a+b));
        System.out.println("a-b="+(a-b));
        System.out.println("a*b="+(a*b));
        System.out.println("a/b="+(a/b));
        System.out.println("a%b="+(a%b));
        System.out.println("b%a="+(b%a));
        System.out.println("a++="+(a++));
        System.out.println("a--="+(a--));
        System.out.println("++a="+(++a));
        System.out.println("--a="+(--a));
    } 
}
